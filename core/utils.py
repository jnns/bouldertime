def get_identifier(attempts=0):
    noun = r.choice(nouns)

    if attempts == 0:
        number = r.randint(2, 9)
        return f"{number}-{noun}"

    number = r.randint(2, 999)
    adj1 = r.choice(adjectives)
    if attempts == 1:
        return f"{number}-{adj1}-{noun}"

    adj2 = r.choice(adjectives)
    return f"{number}-{adj1}-{adj2}-{noun}"
